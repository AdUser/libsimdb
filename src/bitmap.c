int bitmap_compare(const unsigned char *a, const unsigned char *b)
{
  unsigned char diff = 0;
  size_t i = 0;
  size_t cnt = 0;

  for (i = 0; i < BITMAP_SIZE; i++, a++, b++) {
    diff = *a ^ *b;
/* bruteforce:
    while (diff) {
      cnt += (diff & 1);
      diff >>= 1;
    }
*/
    cnt += dict[diff];
  }

  return cnt;
}
